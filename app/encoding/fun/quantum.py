class QuantumEncoder(Encoder):
    name = "Quantum"

    def encode(self, data: bytes) -> str:
        hex_str = data.hex()
        result = []
        for ch in hex_str:
            result.append(random.choice(_SUPERPOSITION[ch]))
        return "".join(result)

    def decode(self, text: str) -> bytes:
        rev = {}
        for k, vals in _SUPERPOSITION.items():
            for v in vals:
                rev[v] = k

        hex_str = "".join(rev.get(c, "0") for c in text)
        return bytes.fromhex(hex_str)
